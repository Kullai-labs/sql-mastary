-- 3. ANALYTICAL CASE QUESTIONS & ANSWERS

-- Challenge 1: Calculate total revenue generated by Biryani Zone (rest_id = 10).
-- Expected Output: Sum total of order amounts for that restaurant.
SELECT SUM(amount) AS total_revenue 
FROM swiggy_orders 
WHERE rest_id = 10;

-- Challenge 2: List the names of users from Bangalore who spent more than 500 on a single order.
-- Expected Output: User names meeting the location and amount criteria.
SELECT u.name 
FROM swiggy_users u 
JOIN swiggy_orders o ON u.user_id = o.user_id 
WHERE u.city = 'Bangalore' AND o.amount > 500;

-- Challenge 3: Find the average order value for Italian cuisine restaurants.
-- Expected Output: Average order cost grouped by Italian cuisine.
SELECT AVG(o.amount) AS avg_amount 
FROM swiggy_orders o 
JOIN restaurants r ON o.rest_id = r.rest_id 
WHERE r.cuisine = 'Italian';
